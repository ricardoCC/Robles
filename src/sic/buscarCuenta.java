    private void tCodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tCodActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel)this.jTable1.getModel();
        int filas = modelo.getRowCount();
        filas = filas - 1;
        
        for (int i = 0; i <= filas; i++) {
            modelo.removeRow(0);
        }
        
        String codigo = tCod.getText();
        String sql = "SELECT CODIGOCUENTA, NOMBRECUENTA FROM tbl_cuenta WHERE CODIGOCUENTA LIKE '%"
                + codigo + "%'";
        
        FrameDBManager f = new FrameDBManager();
        f.DBpushFrame(sql, jTable1);
        
        tCod.setText(null);
    }//GEN-LAST:event_tCodActionPerformed

